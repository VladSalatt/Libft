#include "ft_map.h"

void					ft_mapdel(t_map **map)
{
	int					i;

	i = 0;
	while (i < (*map)->size)
		ht_listdel(&((*map)->head[i++]));
	free((*map)->head);
	free(*map);
	map = NULL;
}
