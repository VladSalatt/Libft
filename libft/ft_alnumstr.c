#include "includes/libft.h"

int						ft_alnumstr(const char *str)
{
	int					i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!ft_isalnum(str[i]))
			return (0);
		i++;
	}
	return (1);
}
